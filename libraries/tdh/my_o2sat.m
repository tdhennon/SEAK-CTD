function [o2sat] = my_o2sat(T,S);
% Based off of Garcia/Gordon, 1992.
%T = 29.1760; S = 34.6970;

Ts = log((298.15-T).*(273.15+T).^-1);

A0 = 5.80818;
A1 = 3.20684;
A2 = 4.11890;
A3 = 4.93845;
A4 = 1.01567;
A5 = 1.41575;

B0 = -7.01211e-3;
B1 = -7.25958e-3;
B2 = -7.93334e-3;
B3 = -5.54491e-3;

C0 = -1.32412e-7;

lno2sat = A0 + A1*Ts.^1 + A2*Ts.^2 + ...
             A3*Ts.^3 + A4*Ts.^4 + A5*Ts.^5 + ...
     S.*(B0 + B1*Ts.^1 + B2*Ts.^2 + B3*Ts.^3) + ...
     C0*S.^2;
 
o2sat = exp(lno2sat);
